# Tipe data Integer
print(2024)
print(type(2024))

# Tipe data Float
print(3.14)
print(type(3.14))

# Tipe data Complex
print(2 + 3j)
print(type(2 + 3j))

# Tipe data String
print("Sedikit demi sedikit paham python, gapapa asli")
print(type("Sedikit demi sedikit paham python, gapapa asli"))

# Tipe data Boolean
print(True)
print(type(True))

# Tipe data List
print([1,2,3,4,5])
print(type([1,2,3,4,5]))